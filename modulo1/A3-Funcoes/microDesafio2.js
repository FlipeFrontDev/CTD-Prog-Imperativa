let soma = function(num1, num2) {

  return num1 + num2;

}

let subtracao = function(num1, num2) {

  return num1 - num2;

}

let multiplicacao = function(num1, num2) {

  return num1 * num2;

}

let divisao = function(num1, num2) {

  return num1 / num2;

}

